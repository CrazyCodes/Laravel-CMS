<?php
    
    namespace App\Admin\Controllers;
    
    use App\Models\ArticleCategory;
    use Encore\Admin\Controllers\AdminController;
    use Encore\Admin\Form;
    use Encore\Admin\Grid;
    use Encore\Admin\Layout\Content;
    use Encore\Admin\Show;
    
    class ArticleCategoryController extends AdminController
    {
        /**
         * Title for current resource.
         *
         * @var string
         */
        protected $title = '分类管理';
        
        public function index(Content $content)
        {
            $content->breadcrumb(
                ['text' => '分类管理']
            );
            
            return parent::index($content); // TODO: Change the autogenerated stub
        }
        
        public function create(Content $content)
        {
            $content->breadcrumb(
                ['text' => '分类管理', 'url' => '/article-categories'],
                ['text' => '创建']
            );
            
            return parent::create($content); // TODO: Change the autogenerated stub
        }
        
        public function edit($id, Content $content)
        {
            $content->breadcrumb(
                ['text' => '分类管理', 'url' => '/article-categories'],
                ['text' => '更新']
            );
            
            return parent::edit($id, $content); // TODO: Change the autogenerated stub
        }
        
        /**
         * Make a grid builder.
         *
         * @return Grid
         */
        protected function grid()
        {
            $grid = new Grid(new ArticleCategory);
            $grid->expandFilter();
            $grid->disableExport();
            $grid->actions(function ($actions) {
                $actions->disableView();
            });
            
            $grid->model()->where('site_id', site()->get());
            $grid->filter(function ($filter) {
                $filter->disableIdFilter();
                $filter->like('title', '标题');
            });
            $grid->column('id', __('编码'))->sortable();
            $grid->column('name', __('分类名称'))->editable();
            $grid->column('sort', __('排序'))->sortable();
            $grid->column('created_at', __('创建时间'));
            
            return $grid;
        }
        
        
        /**
         * Make a form builder.
         *
         * @return Form
         */
        protected function form()
        {
            $form = new Form(new ArticleCategory);
            $form->disableViewCheck();
            $form->tools(function (Form\Tools $tools) {
                $tools->disableView();
            });
            $form->hidden('site_id')->default(site()->get());
            
            $form->text('name', __('分类名称'));
            $form->image('pic_url', __('缩略图'));
            $form->number('sort', __('排序'))->default(0);
            
            return $form;
        }
    }
